package Hoja2_Ajedrez;


public class Figura {

    private int xCord;
    private int yCord;
    
/* ---------------------------- Constructores --------------------------------*/    
    public Figura(int xCord, int yCord) {
        this.xCord = xCord;
        this.yCord = yCord;
    }
/* ------------------------------- Metodos ---------------------------------- */

    public int getxCord() {
        return xCord;
    }

    public int getyCord() {
        return yCord;
    }

    

}// class
